package com.finersoft.chap05;
// 整数常量（十进制／八进制／十六进制）

class DecOctHexLiteral {

	public static void main(String[] args) {
		// 十进制数的13
		int a = 13;		
		//  八进制数的13
		int b = 013;	
			// 十六进制数的13
		int c = 0x13;

		System.out.println("a = " + a);
		System.out.println("b = " + b);
		System.out.println("c = " + c);
	}
}
